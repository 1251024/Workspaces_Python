# built in (내장함수)


# int() : 정수로 바꿔주는 애
print(int(True))
print(int(False))
print(int(12.34))
print(int('56'))

# int의 2진수, 8진수, 16진수
print(int('1111', 2))
print(int('77', 8))
print(int('ff', 16))


# float() : 실수로 바꿔주는 애
print(float('3'))

# str() : 문자로 바꿔주는 애
a = 100
# print('a:'+ a)
print('a:' + str(a))
